    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String search = txtBusqueda.getText().trim();

        if (search.isEmpty()) {
            lblMessage.setForeground(Color.RED);
            lblMessage.setText("Por favor, ingrese un nombre para buscar.");
            return;
        }

        List<String[]> datos = gestionador.buscarPorNombre(search);
        if (datos.isEmpty()) {
            lblMessage.setForeground(Color.RED);
            lblMessage.setText("No hay coincidencias");
            EmpleadoTableModel model = new EmpleadoTableModel(new ArrayList<>());
            tblEmpleados.setModel(model);
        } else {
            lblMessage.setText("Empleado encontrado correctamente");
            EmpleadoTableModel model = new EmpleadoTableModel(datos);
            tblEmpleados.setModel(model);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

