    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            if (jTxtCodigo.getText().isEmpty() || jTxtNombre.getText().isEmpty() || jTxtEdad.getText().isEmpty() || jTxtPeso.getText().isEmpty() || jTxtTalla.getText().isEmpty()) {
                lblMessage.setForeground(Color.RED);
                lblMessage.setText("Por favor, complete todos los campos.");
                return;
            }

            int codigo = Integer.parseInt(jTxtCodigo.getText());
            String nombre = jTxtNombre.getText();
            int edad = Integer.parseInt(jTxtEdad.getText());
            float peso = Float.parseFloat(jTxtPeso.getText());
            float talla = Float.parseFloat(jTxtTalla.getText()) / 100;
            boolean isBornout = false;
            String causaBornout = "";
            int idBornout = 0;

            if (edad <= 0 || peso <= 0 || talla <= 0) {
                lblMessage.setForeground(Color.RED);
                lblMessage.setText("Edad, peso y talla deben ser valores positivos.");
                return;
            }

            if (rdBtnSi.isSelected()) {
                isBornout = true;
            } else if (rdBtnNo.isSelected()) {
                isBornout = false;
            }

            if (jRadioButton1.isSelected()) {
                causaBornout = "Sobrecarga laboral";
                idBornout = 1;
            } else if (jRadioButton2.isSelected()) {
                causaBornout = "No tener tareas completas";
                idBornout = 2;
            } else if (jRadioButton3.isSelected()) {
                causaBornout = "Mucha Presión";
                idBornout = 3;
            } else if (jRadioButton4.isSelected()) {
                causaBornout = "Maltratado por sus superiores";
                idBornout = 4;
            }

            lblMessage.setText(gestionador.ingresarDato(codigo, nombre, edad, peso, talla, isBornout, causaBornout, idBornout));
            jTxtCodigo.setText("");
            jTxtNombre.setText("");
            jTxtEdad.setText("");
            jTxtPeso.setText("");
            jTxtTalla.setText("");
        } catch (NumberFormatException e) {
            lblMessage.setForeground(Color.RED);
            lblMessage.setText("Error: Verifique que ha ingresado números válidos en los campos requeridos.");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

